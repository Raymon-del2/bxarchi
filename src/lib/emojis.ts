export const getAllEmojis = (): string[] => {
  return Object.values(emojiCategories).flat();
};

export const searchEmojis = (query: string): string[] => {
  if (!query.trim()) return getAllEmojis();
  
  const lowerQuery = query.toLowerCase();
  const allEmojis = getAllEmojis();
  
  // Search by keywords
  return allEmojis.filter(emoji => {
    const keywords = emojiData[emoji];
    if (!keywords) return false;
    
    return keywords.some(keyword => keyword.includes(lowerQuery));
  });
};
